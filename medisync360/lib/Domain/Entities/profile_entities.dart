class ProfileEntities {
  final String id;
  final String name;
  final String email;
  final String phone;
  final int age;
  final String gender;
  final String address;
  final String avatarUrl;

  ProfileEntities({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.age,
    required this.gender,
    required this.address,
    required this.avatarUrl,
  });
}
